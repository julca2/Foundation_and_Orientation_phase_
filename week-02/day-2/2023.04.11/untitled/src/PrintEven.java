public class PrintEven {
    public static void main(String[] args) {
        // Create a program that prints all even numbers between 0 and 500 (both included)
        // Each number should be on a new line

        int a = -1;
        while (a < 501) {
            a++;
        if (a % 2 ==0)
            System.out.println(a);
        }


    }
}
